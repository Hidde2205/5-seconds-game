// DOM-elementen
const languageToggle = document.getElementById("language-toggle");
const gameTitle = document.getElementById("game-title");
const newQuestionButton = document.getElementById("new-question-button");
const startTimerButton = document.getElementById("start-timer-button");
const questionElement = document.getElementById("question");
const timerElement = document.getElementById("timer");
const statusMessage = document.getElementById("status-message");

let usedQuestions = [];
let timer;
let timeLeft = 5;
let currentQuestion = ''; // Huidige vraag opslaan
let currentQuestionIndex = -1; // De index van de huidige vraag

// Functie om interface te updaten op basis van taal
function updateInterfaceLanguage() {
    const texts = interfaceTexts[currentLanguage];
    gameTitle.textContent = texts.title;
    questionElement.textContent = texts.startingText;
    newQuestionButton.textContent = texts.newQuestionButton;
    startTimerButton.textContent = texts.startButton;

    // Vertaal de vraag naar de juiste taal
    if (currentQuestionIndex !== -1) {
        const questions = currentLanguage === "nl" ? questionsNL : questionsEN;
        questionElement.textContent = questions[currentQuestionIndex];
    }
}

// Nieuwe vraag genereren
newQuestionButton.addEventListener("click", () => {
    const questions = currentLanguage === "nl" ? questionsNL : questionsEN;

    // Controleer of alle vragen zijn gebruikt
    if (usedQuestions.length === questions.length) {
        usedQuestions = [];
        alert(currentLanguage === "nl" ?
            "Alle vragen zijn gebruikt! De lijst wordt opnieuw gestart." :
            "All questions have been used! The list will reset.");
    }

    // Kies een nieuwe vraag
    let randomIndex;
    do {
        randomIndex = Math.floor(Math.random() * questions.length);
    } while (usedQuestions.includes(randomIndex));

    usedQuestions.push(randomIndex);
    currentQuestionIndex = randomIndex; // Bewaar de index van de gekozen vraag
    questionElement.textContent = questions[randomIndex];

    // Reset status en timer
    statusMessage.textContent = "";
    timerElement.textContent = timeLeft;
    timerElement.classList.add("hidden");

    // Knoppen aanpassen
    newQuestionButton.classList.add("hidden");
    startTimerButton.classList.remove("hidden");
});

// Timer starten
startTimerButton.addEventListener("click", () => {
    timerElement.classList.remove("hidden");
    startTimerButton.classList.add("hidden");

    timeLeft = 5;
    timerElement.textContent = timeLeft;

    timer = setInterval(() => {
        timeLeft--;
        timerElement.textContent = timeLeft;

        if (timeLeft <= 0) {
            clearInterval(timer);
            timerElement.textContent = interfaceTexts[currentLanguage].timeUp;
            statusMessage.textContent = interfaceTexts[currentLanguage].timeUpMessage;

            // Knoppen aanpassen
            newQuestionButton.classList.remove("hidden");
        }
    }, 1000);
});

// Taal toggle
languageToggle.addEventListener("change", (e) => {
    currentLanguage = e.target.value;
    updateInterfaceLanguage(); // Update de interface met de nieuwe taal
});

// Initialiseer met standaardtaal
updateInterfaceLanguage();